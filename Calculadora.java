package classes;

public class Calculadora {
    public int SomaDoisNum(int num1, int num2){  System.out.println("a soma é = "+ (num1+num2)); return num1+num2; }
    public int SubDoisNum(int num1, int num2){ System.out.println("a sub é = "+ (num1-num2)); return num1-num2; }
    public float DivDoisNum(float num1, float num2){ System.out.println("a div é = "+ (num1/num2)); return num1/num2; }
    public float MultDoisNum(int num1, int num2){ System.out.println("a mult é = "+ (num1*num2)); return num1*num2; }
}
